public class Calculator {
    public double getSum(int a, int b) {
        return a + b;
    }

    public double getSubtraction(int a, int b) {
        return a - b;
    }

    public double getMultiplication(int a, int b) {
        return a * b;
    }

    public double getDivision(int a, int b) {
        return a / b;
    }
}
